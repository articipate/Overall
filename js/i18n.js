function getCurrentLang() {
  return localStorage.getItem("overall_lang") || "en";
}

function setLanguage(lang) {
  localStorage.setItem("overall_lang", lang);
  location.reload();
}

function t(key) {
  var lang = getCurrentLang();
  return (I18N[lang] && I18N[lang][key]) || I18N["en"][key] || key;
}

function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach(function(el) {
    var key = el.getAttribute("data-i18n");
    var translated = t(key);
    if (el.tagName === "INPUT") {
      el.placeholder = translated;
    } else {
      el.textContent = translated;
    }
  });
  document.querySelectorAll("[data-i18n-html]").forEach(function(el) {
    var key = el.getAttribute("data-i18n-html");
    el.innerHTML = t(key);
  });
}

function renderLangSelector() {
  var current = getCurrentLang();
  var langs = [
    { code: "tr", label: "TR" },
    { code: "en", label: "EN" },
    { code: "zh", label: "\u4e2d\u6587" }
  ];
  var html = '<div class="lang-selector">';
  langs.forEach(function(l) {
    var cls = l.code === current ? "lang-btn active" : "lang-btn";
    html += '<button class="' + cls + '" onclick="setLanguage(\'' + l.code + '\')">' + l.label + '</button>';
  });
  html += '</div>';

  var nav = document.querySelector('.navbar');
  if (nav) {
    nav.insertAdjacentHTML('beforeend', html);
  }
}

document.addEventListener('DOMContentLoaded', function() {
  renderLangSelector();
  applyTranslations();
});
